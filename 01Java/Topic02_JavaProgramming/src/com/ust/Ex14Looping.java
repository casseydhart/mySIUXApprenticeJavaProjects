package com.ust;

public class Ex14Looping {

	public static void main(String[] args) {
		for (int i=0 ; i<21; i++) {
			System.out.println(i);
		}
		System.out.println("\n");
		for (int j=0 ; j<50; j++) {
			System.out.println(j);
		}
		System.out.println("\n");
		for (int k=1 ; k<21; k++) {
			if(k % 4 == 2);
		System.out.println(k);
		}
	}

}
